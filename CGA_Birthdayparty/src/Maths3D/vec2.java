package Maths3D;

public class vec2 {

    private float m_x;
    private float m_y;


    public vec2(float m_x, float m_y) {
        this.m_x = m_x;
        this.m_y = m_y;

    }

    public float getX() {
        return m_x;
    }

    public void setX(float m_x) {
        this.m_x = m_x;
    }

    public float getY() {
        return m_y;
    }

    public void setY(float m_y) {
        this.m_y = m_y;
    }


}
